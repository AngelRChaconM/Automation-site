const DICTS: Record<Lang, Dict> = { en: EN, es: ES };

/** Replace `{key}` placeholders in translated strings. */
export const t = (lang: Lang, key: string, vars?: Record<string, string | number>): string => {
  let text = DICTS[lang][key] ?? DICTS.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.split(`{${k}}`).join(String(v));
    }
  }
  return text;
};
